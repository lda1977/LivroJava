/**
 * Demonstra um bloco de código.
 */
public class BlockDemo {
    public static void main() {
        double i, j, d;
        i = 5;
        j = 10;

        if (i != 0) {
            System.out.println("I não é igual a zero: ");
            d = j / i;
            System.out.println("J / i é : " + d);
        }/* Fim IF */
    }

}
